    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed
        // TODO add your handling code here:
        String nomeAtual = campoNome.getText();
        String novoNome = campoNovoNome.getText();

        if (nomeAtual.isEmpty() || novoNome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha os dois campos.");
            return;
        }

        int idUsuario = Model.Sessao.getIdUsuarioLogado();
        DAO.PlaylistsDAO dao = new DAO.PlaylistsDAO();
        Model.Playlist playlist = dao.buscarPlaylistPorNome(nomeAtual, idUsuario);

        if (playlist == null) {
            JOptionPane.showMessageDialog(this, "Playlist n√£o encontrada.");
            return;
        }

        boolean sucesso = dao.editarPlaylist(playlist.getIdPlaylist(), novoNome);

        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Nome da playlist atualizado com sucesso!");
            TelaFuncoes a = new TelaFuncoes();
            a.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar a playlist.");
        }
    }//GEN-LAST:event_botaoEditarActionPerformed

